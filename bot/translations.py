def get_text(lang_code: str, key: str) -> str:
    """Get translated text for a given language and key, fallback to English"""
    lang = lang_code.lower() if lang_code else 'en'
    
    # Try exact match
    if lang in TRANSLATIONS and key in TRANSLATIONS[lang]:
        return TRANSLATIONS[lang][key]
    
    # Try first two characters (e.g., 'pt-BR' -> 'pt')
    lang_short = lang[:2]
    if lang_short in TRANSLATIONS and key in TRANSLATIONS[lang_short]:
        return TRANSLATIONS[lang_short][key]
    
    # Fallback to English
    return TRANSLATIONS['en'].get(key, '')
